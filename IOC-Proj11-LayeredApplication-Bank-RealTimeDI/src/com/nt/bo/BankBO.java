package com.nt.bo;

public class BankBO {
	private String cname;
	private String cadd;
	private float si;
	private double ci;
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getCadd() {
		return cadd;
	}
	public void setCadd(String cadd) {
		this.cadd = cadd;
	}
	public float getSi() {
		return si;
	}
	public void setSi(float si) {
		this.si = si;
	}
	public double getCi() {
		return ci;
	}
	public void setCi(double ci) {
		this.ci = ci;
	}
}
